package ru.netology.model;

import jakarta.validation.constraints.NotEmpty;

import java.util.Objects;

public class OperationConfirmationRequest {
    @NotEmpty
    private String operationId;

    @NotEmpty
    private String code;

    public OperationConfirmationRequest(String operationId, String code) {
        this.operationId = operationId;
        this.code = code;
    }

    public String getOperationId() {
        return operationId;
    }

    public void setOperationId(String operationId) {
        this.operationId = operationId;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Override
    public boolean equals(Object o) {
        if (o == null || getClass() != o.getClass()) return false;
        OperationConfirmationRequest that = (OperationConfirmationRequest) o;
        return Objects.equals(operationId, that.operationId) && Objects.equals(code, that.code);
    }

    @Override
    public int hashCode() {
        return Objects.hash(operationId, code);
    }
}
